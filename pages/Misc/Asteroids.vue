        <h6 class="info">
        <span class="highlight_text">Asteroids</span><br>
        Asteroids are minor planets, especially of the inner Solar System. Larger asteroids have also been called planetoids. These terms have historically been applied to any astronomical object orbiting the Sun that did not resemble a planet-like disc and was not observed to have characteristics of an active comet such as a tail. As minor planets in the outer Solar System were discovered they were typically found to have volatile-rich surfaces similar to comets. As a result, they were often distinguished from objects found in the main asteroid belt.<br>
        In this article, the term "asteroid" refers to the minor planets of the inner Solar System including those co-orbital with Jupiter.<br>
        There exist millions of asteroids, many thought to be the shattered remnants of planetesimals, bodies within the young Sun's solar nebula that never grew large enough to become planets. The vast majority of known asteroids orbit within the main asteroid belt located between the orbits of Mars and Jupiter, or are co-orbital with Jupiter (the Jupiter trojans). However, other orbital families exist with significant populations, including the near-Earth objects. Individual asteroids are classified by their characteristic spectra, with the majority falling into three main groups: C-type, M-type, and S-type. These were named after and are generally identified with carbon-rich, metallic, and silicate (stony) compositions, respectively.<br>
        The sizes of asteroids varies greatly; the largest, Ceres, is almost 1,000 km (625 mi) across.<br><br>
        Asteroids are differentiated from comets and meteoroids. In the case of comets, the difference is one of composition: while asteroids are mainly composed of mineral and rock, comets are primarily composed of dust and ice.<br>
        Furthermore, asteroids formed closer to the sun, preventing the development of cometary ice. The difference between asteroids and meteoroids is mainly one of size: meteoroids have a diameter of less than one meter, whereas asteroids have a diameter of greater than one meter.<br>
        Finally, meteoroids can be composed of either cometary or asteroidal materials.
        Only one asteroid, 4 Vesta, which has a relatively reflective surface, is normally visible to the naked eye, and this only in very dark skies when it is favorably positioned. Rarely, small asteroids passing close to Earth may be visible to the naked eye for a short time.[6] As of October 2017, the Minor Planet Center had data on almost 745,000 objects in the inner and outer Solar System, of which almost 504,000 had enough information to be given numbered designations.<br><br>
        <span class="highlight_text">Asteroid belt</span><br>
        The majority of known asteroids orbit within the asteroid belt between the orbits of Mars and Jupiter, generally in relatively low-eccentricity (i.e. not very elongated) orbits.<br>This belt is now estimated to contain between 1.1 and 1.9 million asteroids larger than 1 km (0.6 mi) in diameter,[40] and millions of smaller ones.<br>
        These asteroids may be remnants of the protoplanetary disk, and in this region the accretion of planetesimals into planets during the formative period of the Solar System was prevented by large gravitational perturbations by Jupiter.<br><br>
        <span class="highlight_text">Trojans</span><br>
        Trojans are populations that share an orbit with a larger planet or moon, but do not collide with it because they orbit in one of the two Lagrangian points of stability, L4 and L5, which lie 60° ahead of and behind the larger body.
        The most significant population of trojans are the Jupiter trojans. Although fewer Jupiter trojans have been discovered (As of 2010), it is thought that they are as numerous as the asteroids in the asteroid belt. Trojans have been found in the orbits of other planets, including Venus, Earth, Mars, Uranus, and Neptune.
        <span>Near-Earth Asteroids</span><br>
        Near-Earth asteroids, or NEAs, are asteroids that have orbits that pass close to that of Earth. Asteroids that actually cross Earth's orbital path are known as Earth-crossers. As of June 2016, 14,464 near-Earth asteroids are known[19] and the number over one kilometer in diameter is estimated to be 900–1,000.<br><br>
        <span class="highlight_text">Rotation</span><br>
        Measurements of the rotation rates of large asteroids in the asteroid belt show that there is an upper limit. No asteroid with a diameter larger than 100 meters has a rotation period smaller than 2.2 hours.<br>
        For asteroids rotating faster than approximately this rate, the inertial force at the surface is greater than the gravitational force, so any loose surface material would be flung out. However, a solid object should be able to rotate much more rapidly.<br>
        This suggests that most asteroids with a diameter over 100 meters are rubble piles formed through accumulation of debris after collisions between asteroids.<br><br>
        <span class="highlight_text">Classification</span><br>
        Asteroids are commonly classified according to two criteria: the characteristics of their orbits, and features of their reflectance spectrum.<br>
        Orbital classification - Many asteroids have been placed in groups and families based on their orbital characteristics. Apart from the broadest divisions, it is customary to name a group of asteroids after the first member of that group to be discovered.<br>Groups are relatively loose dynamical associations, whereas families are tighter and result from the catastrophic break-up of a large parent asteroid sometime in the past.<br>Families are more common and easier to identify within the main asteroid belt, but several small families have been reported among the Jupiter trojans.[82] Main belt families were first recognized by Kiyotsugu Hirayama in 1918 and are often called Hirayama families in his honor.
        About 30–35% of the bodies in the asteroid belt belong to dynamical families each thought to have a common origin in a past collision between asteroids. A family has also been associated with the plutoid dwarf planet Haumea.<br><br>
        <span class="highlight_text">Quasi-satellites and horseshoe objects</span><br>
        Some asteroids have unusual horseshoe orbits that are co-orbital with Earth or some other planet. Examples are 3753 Cruithne and 2002 AA29. The first instance of this type of orbital arrangement was discovered between Saturn's moons Epimetheus and Janus.
        Sometimes these horseshoe objects temporarily become quasi-satellites for a few decades or a few hundred years, before returning to their earlier status. Both Earth and Venus are known to have quasi-satellites.
        Such objects, if associated with Earth or Venus or even hypothetically Mercury, are a special class of Aten asteroids. However, such objects could be associated with outer planets as well.
        <span class="highlight_text">Spectral classification</span><br>
        In 1975, an asteroid taxonomic system based on color, albedo, and spectral shape was developed by Clark R. Chapman, David Morrison, and Ben Zellner.[83] These properties are thought to correspond to the composition of the asteroid's surface material. The original classification system had three categories: C-types for dark carbonaceous objects (75% of known asteroids), S-types for stony (silicaceous) objects (17% of known asteroids) and U for those that did not fit into either C or S. This classification has since been expanded to include many other asteroid types. The number of types continues to grow as more asteroids are studied.
        The two most widely used taxonomies now used are the Tholen classification and SMASS classification. The former was proposed in 1984 by David J. Tholen, and was based on data collected from an eight-color asteroid survey performed in the 1980s. This resulted in 14 asteroid categories.[84] In 2002, the Small Main-Belt Asteroid Spectroscopic Survey resulted in a modified version of the Tholen taxonomy with 24 different types. Both systems have three broad categories of C, S, and X asteroids, where X consists of mostly metallic asteroids, such as the M-type. There are also several smaller classes.[85]
        The proportion of known asteroids falling into the various spectral types does not necessarily reflect the proportion of all asteroids that are of that type; some types are easier to detect than others, biasing the totals. <br><br>
        <span class="highlight_text">What Is The Difference Between Asteroids and Meteorites?</span> <br>
        In simplest terms here are the definitions:<br>
        Asteroid: a large rocky body in space, in orbit around the Sun.<br>
        Meteoroid: much smaller rocks or particles in orbit around the Sun.<br><br>
        Meteor: If a meteoroid enters the Earth’s atmosphere and vaporizes, it becomes a meteor, which is often called a shooting star.<br>
        Meteorite: If a small asteroid or large meteoroid survives its fiery passage through the Earth’s atmosphere and lands on Earth’s surface, it is then called a meteorite.<br><br>
        <span class="highlight_text">Comets</span><br>
        Comets, like asteroids, are small celestial bodies that orbit the Sun. However, unlike asteroids, comets are composed primarily of frozen ammonia, methane or water, and contain only small amounts of rocky material.<br>
        As a result of this composition comets have been given the nickname of "dirty snowballs."<br><br>
        <span class="highlight_text">Parts of a comet</span><br>
        Some comets have highly elliptical orbits that bring them relatively close to the Sun. As these comets near the Sun the ices found within them melt and brilliant features are formed.<br>
        Below is a list of these features.<br><br>
        <ul>
          <li><span class="highlight_link">Nucleus:</span> the nucleus of a comet is composed of ice and rocky material. The nucleus of most comets ranges from about 10 to 100 km in diameter, though they can be as large as 100 km in diameter.</li>
          <li><span class="highlight_link">Coma:</span> the cloud of gases that forms around the nucleus as the coma is heated is know as the coma. These gases are usually a mixture of water vapor, ammonia, carbon dioxide.</li>
          <li><span class="highlight_link">Dust Tail:</span> the dust tail of a comet is composed of gases and tiny dust particles blown away from the nucelus as the comet is heated. The dust tail is the most visible part of a comet.</li>
          <li><span class="highlight_link">Ion Tail:</span> the ion tail is a stream of ionized gases that are blown directly away from the Sun as a result of the comet’s contact with the solar wind.</li>
        </ul><br>
        <span class="highlight_text">Where do comets come from?</span><br>
        Comets have been seen by humankind for millenia. However, scientists have determined that given the “shedding” of material each time a comet orbits closely to the Sun, a comet’s lifespan may only be thousands of years – relatively small number when compared to the lifespan of the Solar System.<br>Thus scientists have concluded that if comets are still present in the Solar System today, then there must be a nursery of comets somewhere in the Solar System, otherwise all of the comets would have run the course of their lives long ago. Furthermore, since we have determined almost all of the inner Solar System objects the only place left for this reservoir of new comets to exist is in the outer Solar System.
        Over the course of the second half of the twentieth century evidence provided by the orbits of comets suggested that there is not just one region of the outer Solar System producing comets, but two.<br>The first of these regions is what is known as the Kuiper Belt, a band of comets similar in many ways to the Asteroid Belt found in the inner Solar System.<br>
        Comets originating in this region have relatively short orbital periods and orbit the Sun in roughly the same plane as do the planets. The second region, called the Oort Cloud, is a region farther out than the Kuiper Belt and is essentially a spherical shell. Those comets coming from this area have orbital periods much longer than those of the Kuiper Belt. Additionally, the orbital plane of comets from the Oort Cloud may be drastically inclined to that of the orbital plane of the planets.
        As for how a comet originating in one of these regions acquires an orbit that brings it into the inner Solar System, current theories propose that it may either be knocked into its new orbit through a collisions with another comet or its orbit may be altered by the gravitational force of an interstellar object passing nearby in the outer Solar System.<br><br>
        <span class="highlight_text">Famous comets</span><br><br>
        There are several famous comets, with some being known to ancient humans and some only recently appearing.<br>
        Below are the most well known comets.<br><br>
        <span class="highlight_text">Halley’s Comet - </span> In 1705, while studying the orbits of several known comets, Edmond Halley found that the comet observed in 1531, 1607 and 1682 was one in the same. As a result of Halley’s discovery, the comet was named after him. Halley’s Comet is visible every 75 to 76 years.<br><br>
        <span class="highlight_text">Comet Hale-Bopp - </span> Comet Hale-Bopp is best known to most people for the much publicized Californian cult who believed the comet to be a spacecraft. Comet Hale-Bopp was last visible in 1997 and will not be visible again for approximately 2,300 years. It is named after its co-discoverers, Alan Hale and Thomas Bopp.<br><br>
        <span class="highlight_text">Comet Shoemaker-Levy 9 - </span> Shoemaker-Levy 9, also known as SL 9, was a string of comets that was captured by the gravity of Jupiter and subsequently began an orbit about the planet. The orbit of SL 9 assumed about Jupiter was, however, highly irregular and as a result of this irregularity SL 9 crashed into Jupiter in a brilliant display during the week of July 16, 1994. Shoemake-Levy 9 is named after its discoverers, Gene Shoemaker, Carolyon Shoemaker and David Levy.<br><br>
